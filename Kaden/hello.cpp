/*The include command tells the program to include a package needed for some commands*/
#include <iostream>
/*using namespace std; tells the program to output cout*/
using namespace std;
/*int main() is the entry point once the program is run*/
int main(){
	/*cout echoes its content to the screen*/
	/*endl and \n are used as line breaks*/
	cout<<"Hello Kaden!"<<endl;
	/*int is used to tell the variable ahead of it to recognise it as an 		integer*/
	int x=10;
	/*<< is used to seperate text/variable outputs*/
	cout<<"multiple "<<"item "<<"text "<<endl;
	cout<<x<<endl;
	cout<<"hello!\n";
	/*return tells the program to exit the current stream*/
	return 0;
}

